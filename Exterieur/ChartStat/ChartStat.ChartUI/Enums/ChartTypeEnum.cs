namespace ChartStat.ChartUI.Enums
{
    /// <summary>
    /// Тип диаграммы.
    /// </summary>
    public enum ChartTypeEnum
    {
        /// <summary>
        /// Столбчатая диаграмма.
        /// </summary>
        ColumnarChart,

        /// <summary>
        /// Линейная диаграмма.
        /// </summary>
        LineChart,

        /// <summary>
        /// Радиальная (сетчатая) диаграмма.
        /// </summary>
        GrooveChart,

